package io.github.idyatlov.localization;

/**
 * Created by Jedo on 08.08.2016.
 */
public class LocalizationExtension {

  private String oneskyPublic;

  private String oneSkySecret;

  private String oneskyProjectId;

  private String path;

  public String getOneskyPublic() {
    return oneskyPublic;
  }

  public void setOneskyPublic(String oneskyPublic) {
    this.oneskyPublic = oneskyPublic;
  }

  public String getOneSkySecret() {
    return oneSkySecret;
  }

  public void setOneSkySecret(String oneSkySecret) {
    this.oneSkySecret = oneSkySecret;
  }

  public String getOneskyProjectId() {
    return oneskyProjectId;
  }

  public void setOneskyProjectId(String oneskyProjectId) {
    this.oneskyProjectId = oneskyProjectId;
  }

  public String getPath() {
    return path;
  }

  public void setPath(String path) {
    this.path = path;
  }
}
